Agaviではデフォルトでデバッグモードが有効になっています。すなわち、すべての設定ファイルがリクエストごとに再パースされます。
このため各ページのロードが遅いと感じることでしょう。
このインストール環境で開発を行う予定ならデバッグモードを有効のままにしておくとよいでしょう。
そうでなければ無効にすることができます。

<br/><br/>

<strong>注意:</strong> Agaviのキャッシュディレクトリおよびそのサブディレクトリ内のファイルを手動で消すこともできます。

<br/><br/>

<strong>注意:</strong> もし、デバッグモードをオフにして開発すると、
不正なキャッシュファイルに起因する多くのエラーが発生するかもしれません。
このため、あなたのアプリケーションが完全に動作するまでデバッグモードを有効にしておくことをお勧めしています。
